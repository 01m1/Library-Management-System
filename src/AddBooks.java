    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String url = "jdbc:mysql://localhost/library";
        String user = "root";
        String query = "insert into books values (?, ?, ?, ?, ?)";
        String id =  jTextField1.getText();
        String category = jTextField3.getText();
        String name = jTextField2.getText();
        String author = jTextField4.getText();
        int copies = Integer.parseInt(jTextField6.getText());
        String checkquery = "update books set copies=copies+"+copies+" where name='"+name+"' and category='"+category+"'";
        try {
            Connection conn = DriverManager.getConnection(url, user, "");
            Statement stmnt = conn.createStatement();
            int rows = stmnt.executeUpdate(checkquery);
            if (rows>0) {
                JOptionPane.showMessageDialog(this, "Recorded Successfully");
            }
            else {
                PreparedStatement stm = conn.prepareCall(query);
                stm.setString(1, id);
                stm.setString(2, category);
                stm.setString(3, name);
                stm.setString(4, author);
                stm.setInt(5, copies);
                stm.execute();
                JOptionPane.showMessageDialog(this, "Recorded Successfully ");
            }
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jTextField6.setText(null);
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

