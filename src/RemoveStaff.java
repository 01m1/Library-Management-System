    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String url = "jdbc:mysql://localhost/library";
        String user = "root";
        String input =  jTextField1.getText();
        String query = "delete from staff where staff_id='"+input+"' or name='"+input+"';";

        try {
            Connection conn = DriverManager.getConnection(url, user, "");
            Statement stmnt = conn.createStatement();
            int rows = stmnt.executeUpdate(query);
            if (rows>0) {
                JOptionPane.showMessageDialog(this, "Staff removed");
            }
            else {
                JOptionPane.showMessageDialog(this, "Staff not available");
            }

            stmnt.close();

        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

